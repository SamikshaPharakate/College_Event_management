const dotenv = require('dotenv');
dotenv.config();
const app = require('./app');
const { connectDB } = require('./db');

const PORT = process.env.PORT || 4000;

(async () => {
  await connectDB();
  app.listen(PORT, () => {
    console.log(`Server listening on port ${PORT}`);
  });
})().catch(err => {
  console.error('Failed to start server', err);
  process.exit(1);
});
